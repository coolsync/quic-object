# 2: Python has a built-in function sum to find sum of all elements of a list. Provide an implementation for sum.

# 有一个能够计算所有元素之和的built-in function sum, 实现这个sum function
# print(sum(num))
def sum(num):
    result = 0
    for i in num:
        result += i
    return result 
     
